    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String marcaymodelo =txtMarcayModelo.getText();
        String kilometraje = txtKilometraje.getText();
        String precio =txtPrecio.getText();
        String nombre =txtNombres.getText();
        String apellidos =txtApellidos.getText();
        String dni = txtDNI.getText();
        String direccion= txtDireccion.getText();
        String telefono = txtTelefono.getText();
        String trabajoactual=txtTrabajoActual.getText();
        String antecedentes =(String)cmbAntecedentes.getSelectedItem();
        String observaciones = txtObservaciones.getText();
        control.guardar(marcaymodelo,kilometraje,precio,nombre,apellidos,
                dni,direccion,telefono,trabajoactual,antecedentes,observaciones );
        
        JOptionPane optionPane = new JOptionPane("Se guardo correctamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guardado exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }//GEN-LAST:event_btnGuardarActionPerformed



